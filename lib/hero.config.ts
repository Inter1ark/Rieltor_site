/**
 * ───────────────────────────────────────────────────────────────────────────
 *  ПУЛЬТ УПРАВЛЕНИЯ HERO-СЕКЦИЕЙ (пошаговый режим)
 * ───────────────────────────────────────────────────────────────────────────
 *  Hero работает «шагами»: при каждом скролле текст растворяется, видео плавно
 *  доматывается до кадра следующего шага, и появляются новые тексты.
 *  Стопы задаются ниже массивом HERO_STEPS — просто меняй `frame` и тексты.
 */

/** Количество кадров в секвенции (frame_0001.avif … frame_0600.avif). */
export const FRAME_COUNT = 600;

export const FRAME_BASENAME = "frame_";
export const FRAME_EXT = "avif";

/** Длительность плавной доводки видео между шагами (сек). */
export const STEP_DURATION = 1.5;

/** Путь к кадру по индексу (0-based) и набору. */
export function framePath(index: number, set: "desktop" | "mobile") {
  const n = String(index + 1).padStart(4, "0");
  return `/frames/${set}/${FRAME_BASENAME}${n}.${FRAME_EXT}`;
}

export type CTA = {
  label: string;
  href: string;
  variant?: "primary" | "ghost";
};

export type Align = "center" | "left" | "right";

/**
 * Один шаг (стоп) Hero.
 *  frame    — номер кадра видео (1…FRAME_COUNT), на котором останавливаемся.
 *  align    — расположение текста на экране.
 */
export type HeroStep = {
  id: string;
  frame: number;
  /** Длительность доводки видео ПРИ ПЕРЕХОДЕ к этому шагу (сек).
   *  Если не указана — берётся STEP_DURATION. Чем больше — тем медленнее
   *  и торжественнее «проезд» (полезно для длинных переходов). */
  duration?: number;
  align?: Align;
  eyebrow?: string;
  title?: ReactNode;
  subtitle?: string;
  ctas?: CTA[];
};

